/**
 * Get SEO metadata for a specific page and language
 * @param {string} page - Page key from seoMetadata
 * @param {string} lang - Language code (az, ru, en)
 * @returns {object} - { title, description, keywords }
 */
export function getSeoMeta(page, lang = "az") {
  const pageMeta = seoMetadata[page];
  if (!pageMeta) {
    return seoMetadata.home[lang] || seoMetadata.home.az;
  }
  return pageMeta[lang] || pageMeta.az;
}

