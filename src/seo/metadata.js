/**
 * Generate product-specific SEO metadata
 * @param {object} product - Product object with title_az, title_ru, title_en
 * @param {string} lang - Language code
 * @returns {object} - { title, description }
 */
export function getProductSeoMeta(product, lang = "az") {
  if (!product) {
    return getSeoMeta("home", lang);
  }

  const productTitle =
    lang === "az"
      ? product.title_az || product.title_en
      : lang === "ru"
        ? product.title_ru || product.title_en
        : product.title_en || product.title_az;

  const descriptions = {
    az: `${productTitle} - Danilov premium əl işi ayaqqabı. Yüksək keyfiyyətli dəri, Bakıda istehsal.`,
    ru: `${productTitle} - Danilov премиальная обувь ручной работы. Высококачественная кожа, произведено в Баку.`,
    en: `${productTitle} - Danilov premium handmade shoe. High-quality leather, made in Baku.`,
  };

  return {
    title: `${productTitle} - Danilov`,
    description: descriptions[lang] || descriptions.az,
  };
}
